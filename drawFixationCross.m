function drawFixationCross(cfg, color)
    % Define the parameters of the fixation cross in `cfg` and `expParameters`

    Screen('DrawLines', ...
        cfg.screen.win, ...
        cfg.allCoords, ...
        cfg.lineWidthPix, ...
        color, ...
        [cfg.screen.center(1) cfg.screen.center(2)], 1);

end
